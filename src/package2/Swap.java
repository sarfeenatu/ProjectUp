package package2;

public class Swap {

	public static void main(String[] args) {
		int a=50,b=100,temp;
		System.out.println("Before swapping:");
		System.out.println("value of a="+a+"\n value of b="+b);
		temp=a;
		a=b;
		b=temp;
		System.out.println("After swapping:");
		System.out.println("value of a="+a+"\n value of b="+b);

	}

}
